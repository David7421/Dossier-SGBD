CREATE OR REPLACE PROCEDURE RECEPTION_FILM
AS
BEGIN
	--TO DO DUPVAL + UPDATE
	INSERT INTO FILMSCHEMA SELECT * FROM tmpXMLMovie@CB.DBL;
	INSERT INTO copieFilm SELECT * FROM tmpXMLCopy@CB.DBL;

